import mongoose from 'mongoose';

const productSchema = mongoose.Schema(
  {
    // 商品基本信息
    name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    brand: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    
    // 价格和库存
    price: {
      type: Number,
      required: true,
      default: 0,
    },
    countInStock: {
      type: Number,
      required: true,
      default: 0,
    },
    
    // 评分和评论
    rating: {
      type: Number,
      required: true,
      default: 0,
    },
    numReviews: {
      type: Number,
      required: true,
      default: 0,
    },
    
    // 微信小程序相关
    isWechatExclusive: {
      type: Boolean,
      default: false,
    },
    wechatPromotionPrice: {
      type: Number,
    },
    
    // 商品状态
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

const Product = mongoose.model('Product', productSchema);

export default Product;
